# len() - Quantidade de caracteres
profissao = "Programador"
print(profissao)
print(len(profissao))

frase = "Minha função é "
print(frase)
print(len(frase))

# Acessando os caracteres
print(profissao[0])
print(profissao[3])

# Concatenação
print(frase + profissao)

# Strings dinânicas
nome = "Jefferson"
idade = 30

print("Meu nome é %s " % nome)
print("A minha idade é %d anos" % idade)

carro = "Sandero"
ano = 2017

print("Meu carro é um %s e ele foi fabricado no ano de %d" % (carro, ano))

# Strings com float
valor = 200.00

print("O valor do curso é %.2f reais" % valor) 